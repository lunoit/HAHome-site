const langNames = { en: "EN", nl: "NL", fr: "FR", de: "DE", es: "ES" };
const langFlags = { en: "🇬🇧", nl: "🇳🇱", fr: "🇫🇷", de: "🇩🇪", es: "🇪🇸" };

function setLanguage(lang) {
  if (!translations[lang]) return;
  localStorage.setItem("hahome-lang", lang);
  document.documentElement.lang = lang;

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    const val = translations[lang][key];
    if (val !== undefined) el.textContent = val;
  });

  document.querySelectorAll("[data-i18n-html]").forEach((el) => {
    const key = el.getAttribute("data-i18n-html");
    const val = translations[lang][key];
    if (val !== undefined) el.innerHTML = val;
  });

  // Update active state in picker
  document.querySelectorAll(".lang-option").forEach((btn) => {
    btn.classList.toggle("active", btn.dataset.lang === lang);
  });

  // Update the current language display
  const currentEl = document.querySelector(".lang-current");
  if (currentEl) {
    currentEl.textContent = langFlags[lang] + " " + langNames[lang];
  }

  // Close dropdown
  const dropdown = document.querySelector(".lang-dropdown");
  if (dropdown) dropdown.classList.remove("open");
}

document.addEventListener("DOMContentLoaded", () => {
  const saved = localStorage.getItem("hahome-lang");
  const browserLang = navigator.language.slice(0, 2);
  const lang = saved || (translations[browserLang] ? browserLang : "en");
  setLanguage(lang);
});
